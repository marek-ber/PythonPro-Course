# 3. Prosty kalkulator obwodu: Napisz program, który poprosi użytkownika o długość i
# szerokość prostokąta, a następnie obliczy i wyświetli jego obwód (P = 2 * (długość +
# szerokość)).

dlugosc = float(input("Podaj długość prostokąta: "))
szerokosc = float(input("Podaj szerokość prostokąta: "))

obwod = 2 * (dlugosc + szerokosc)

print(obwod)


